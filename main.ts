function Add_Item_To_Inventory (ATK: number, DEF: number, BD: number, SPL: number, CR: number, BladeName: string, Type: string) {
    if (blockSettings.readNumber("WeaponInventory") < 9) {
        blockSettings.readNumberArray("ATKSTATS")[blockSettings.readNumber("WeaponInventory")] = ATK + blockSettings.readNumber("ATKLV") + (blockSettings.readNumber("Level") + 3)
        blockSettings.readNumberArray("DEFSTATS")[blockSettings.readNumber("WeaponInventory")] = DEF + blockSettings.readNumber("DEFLV") + (blockSettings.readNumber("Level") + 3)
        blockSettings.readNumberArray("BDSTATS")[blockSettings.readNumber("WeaponInventory")] = BD + blockSettings.readNumber("BDLV") + (blockSettings.readNumber("Level") + 0)
        blockSettings.readNumberArray("SPLSTATS")[blockSettings.readNumber("WeaponInventory")] = SPL + blockSettings.readNumber("SPLLV") + (blockSettings.readNumber("Level") + 3)
        blockSettings.readNumberArray("CRLVSTATS")[blockSettings.readNumber("WeaponInventory")] = CR + blockSettings.readNumber("CRLV") + (blockSettings.readNumber("Level") + 0)
        blockSettings.readStringArray("WeaponNames")[blockSettings.readNumber("WeaponInventory")] = BladeName
        blockSettings.readStringArray("WeaponTypes")[blockSettings.readNumber("WeaponInventory")] = Type
        blockSettings.writeNumber("WeaponInventory", blockSettings.readNumber("WeaponInventory") + 1)
        blockSettings.writeBoolean(BladeName, true)
    }
}
